#include "libft.h"

void				*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*d;

	if (len == 0)
		return (b);
	d = (unsigned char *)b;
	while (len--)
	{
		*d = (unsigned char)c;
		d++;
	}
	return (b);
}
