#include "libft.h"

size_t			ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		dst_len;
	size_t		src_len;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	if (size <= 0)
		return (src_len);
	if (dst_len > size - 1)
		return (size + src_len);
	dst = ft_strncat(dst + dst_len, src, size - dst_len - 1);
	return (dst_len + src_len);
}
