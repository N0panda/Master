#include "libft.h"

char			*ft_strdup(const char *s1)
{
	size_t		len;
	char		*s2;

	len = ft_strlen(s1);
	s2 = (char *)malloc(len * sizeof(char) + 1);
	if (s2 != NULL)
		s2 = ft_strcpy(s2, s1);
	return (s2);
}
